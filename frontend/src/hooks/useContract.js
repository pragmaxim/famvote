// This will be the address of your deployed contract
// For local development with Anvil, you'll need to update this after deployment
const CONTRACT_ADDRESS = '0x5FbDB2315678afecb367f032d93F642f64180aa3'; // Default Anvil first address

export function useContract() {
  const publicClient = usePublicClient();
  const { data: walletClient } = useWalletClient();
  const [contract, setContract] = useState(null);

  useEffect(() => {
    if (!publicClient) return;

    const readContract = getContract({
      address: CONTRACT_ADDRESS,
      abi: FamilyVoteABI,
      client: publicClient,
    });

    setContract(readContract);
  }, [publicClient]);

  return {
    contract,          // read-only
    abi: FamilyVoteABI,
    address: CONTRACT_ADDRESS,
    walletClient,      // use this to send transactions
  };
}